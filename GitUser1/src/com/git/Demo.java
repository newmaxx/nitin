package com.git;

public class Demo {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		System.out.println(addition(a, b));
		System.out.println(substratioc(a, b));
	}

	public static int addition(int a, int b) {
		int c = a + b;
		return c;
	}

	public static int substratioc(int a, int b) {

		int c = a - b;
		return c;
	}

}
